
  #include "p18f4550.inc"

SUMANDO EQU 0X1F
TAREA EQU 0X79
TAREADOS EQU 0X07
  ORG 0

MAIN
  ; NOt de ambas cantidades, suma, resta multiplicaci√≥n
  MOVLW TAREA
  ;NOT ambas
    COMF TAREA
    COMF TAREADOS
  ;MULTIPLICACION
    MOVLW TAREA
    MULLW TAREADOS
  ;SUMAR
    MOVLW TAREA
    ADDLW TAREADOS
  ;RESTAR mayor de menor
    MOVLW TAREA
    SUBLW TAREADOS
  ;RESTAR menor de mayor
    MOVLW TAREADOS
    SUBLW TAREA
  ;XOR
    MOVLW TAREA
    XORLW TAREADOS
  ;OR
    MOVLW TAREA
    IORLW TAREADOS
  ;AND
    MOVLW TAREA
    ANDLW TAREADOS
  GOTO MAIN
  END
