; Par impar

#include "p18f4550.inc"
PARBINARIO EQU B'10101010'
IMPARBINARIO EQU B'01010101'

ORG 0
CONFIGURACION
  MOVLW B'00001000'
  MOVWF TRISA,0 
  CLRF PORTA,0 ;MOVLW B'0000000' MOVWF PORTA,0
  MOVLW QUINCE ;W=15
  MOVWF ADCON1,0 ;ADCON1=15 POR LO QUE LAS ENTRADAS DE LOS PUERTOS A,B,E SON DIGITALES
  MOVLW H'07' ;W=07
  MOVWF CMCON,0 ;EL COMPRADOR DEL PORTA A Y E SE APAGA
  CLRF PORTB,0
  MOVLW B'00000000'
  MOVWF TRISB,0 ;TRISB=0, ENTONCES PB=SSSS SSSS

MAIN
  BTFSS PORTA,4,0
  GOTO IMPAR
  GOTO PAR

IMPAR
  MOVLW PARBINARIO
  MOVFF WREG,PORTB
  GOTO MAIN
PAR
  MOVLW IMPARBINARIO
  MOVFF WREG,PORTB
  GOTO MAIN
  END