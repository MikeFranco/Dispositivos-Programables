

DATOA EQU .20
DATOB EQU .25
VEINTE EQU .20
ONCE EQU .11
SUMMA EQU .6
RESSTA EQU .27
PRODUCTO EQU .150
ORG .0

SUMAR
  MOVLW VEINTE
  MOVWF DATOA,1
  MOVLW ONCE
  MOVWF DATOB,1
  ADDWF DATOA,0,1
  MOVWF SUMMA,1

RESTAR
  MOVLW VEINTE
  MOVWF DATOA,1
  MOVLW ONCE
  MOVWF DATOB,1
  SUBWF DATOA,0,1
  MOVWF RESSTA,1

MULTIPLICACION
  MOVLW VEINTE
  MOVWF DATOA,1
  MOVLW ONCE
  MOVWF DATOB,1
  MULWF WREG,DATOA
  MOVFF PRODL,WREG
  GOTO SUMAR
  END
