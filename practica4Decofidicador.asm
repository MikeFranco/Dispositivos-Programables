ORG .0
GOTO INICIO
TABLA ; TODO ESTO ES POSIBLE POR EL PC
  AADWF, PCL,F,0 ; PC 40
  DT CERO,UNO,DOS,TRES,CUATRO,CINCO,SEIS,SIETE,OCHO,NUEVE
